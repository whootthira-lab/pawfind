// API HANDLER
// ══════════════════════════════════════════════════════════════
export async function POST(req: Request) {
  try {
    const apiKey = process.env.OPENAI_API_KEY
    if (!apiKey) throw new Error('Missing OPENAI_API_KEY')

    const { message, characterId = 'cat', pageContext = '/', history = [] } = await req.json()

    // Build message history for context
    const historyMessages = history.map((m: { role: string; text: string }) => ({
      role: m.role === 'bot' ? 'assistant' : 'user',
      content: m.text,
    }))

    const response = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        Authorization: `Bearer ${apiKey}`,
      },
      body: JSON.stringify({
        model: 'gpt-4o-mini',
        messages: [
          {
            role: 'system',
            content: getSystemPrompt(characterId, pageContext),
          },
          ...historyMessages,
          {
            role: 'user',
            content: message,
          },
        ],
        temperature: characterId === 'owl' ? 0.5 : characterId === 'dog' ? 0.8 : 0.7,
        max_tokens: 400,
      }),
    })

    if (!response.ok) {
      const err = await response.json()
      throw new Error(err.error?.message || 'OpenAI API error')
    }

    const data = await response.json()
    const reply = data.choices[0].message.content

    return NextResponse.json({ reply })
  } catch (error: unknown) {
    const message = error instanceof Error ? error.message : 'Unknown error'
    console.error('[Chat API]', message)
    return NextResponse.json(
      { error: `ระบบขัดข้องชั่วคราวครับ: ${message}` },
      { status: 500 }
    )
  }
}
